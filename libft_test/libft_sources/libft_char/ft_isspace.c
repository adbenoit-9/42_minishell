#include "../../libft_header/libft.h"

/*
** Vérifie si le caractère c est un espace.
** =========
** #1 : le carctère à analyser.
** =========
** Retourne 1 si c'est le cas (VRAI),
** 0 si ce n'est pas le cas (FAUX).
*/

int	ft_isspace(int c)
{
	if (c == ' ')
		return (1);
	return (0);
}